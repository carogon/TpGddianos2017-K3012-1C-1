CREATE TRIGGER GDDIANOS.CHECK_PERSONA_CLIENTE_FACTURA ON GDDIANOS.FACTURA INSTEAD OF INSERT
AS
BEGIN
	DECLARE @PERS_TIPO INT,@FACT_PERS_ID INT;DECLARE @TIPO_PERSONA INT;DECLARE @FACT_ID_INS INT;DECLARE @MES INT;DECLARE @ANIO INT; DECLARE @FACT_FECHA_GENERADA DATETIME;DECLARE @FACT_FECHA_INICIO DATETIME;DECLARE @FACT_FECHA_FIN DATETIME;DECLARE @FACT_IMPORTE_TOTAL DECIMAL(18,2);
	DECLARE FACTURAS_A_INSERTAR CURSOR FOR SELECT inserted.*,p.pers_tipo FROM INSERTED join persona p on p.pers_id=inserted.fact_pers_id 
	OPEN FACTURAS_A_INSERTAR
	FETCH NEXT FROM FACTURAS_A_INSERTAR INTO @FACT_ID_INS,@FACT_FECHA_GENERADA,@FACT_FECHA_INICIO,@FACT_FECHA_FIN,@FACT_IMPORTE_TOTAL,@FACT_PERS_ID,@PERS_TIPO
	--SELECT @TIPO_PERSONA = PERS_TIPO FROM GDDIANOS.PERSONA WHERE PERS_ID=@FACT_PERS_ID;
	WHILE (@@FETCH_STATUS=0)
	BEGIN
		IF(@PERS_TIPO=1 OR @PERS_TIPO=3) 
		BEGIN 
	 
			INSERT INTO GDDIANOS.FACTURA ([FACT_ID]
			  ,[FACT_FECHA_GENERADA]
			  ,[FACT_FECHA_INICIO]
			  ,[FACT_FECHA_FIN]
			  ,[FACT_IMPORTE_TOTAL]
			  ,[FACT_PERS_ID])
			   VALUES(
			   @FACT_ID_INS
			  ,@FACT_FECHA_GENERADA
			  ,@FACT_FECHA_INICIO
			  ,@FACT_FECHA_FIN
			  ,@FACT_IMPORTE_TOTAL
			  ,@FACT_PERS_ID)
			  
			  ;
	
				INSERT INTO GDDIANOS.ITEM_FACTURA --INSERTAR LOS ITEMS DE LA FACTURA
				SELECT @FACT_ID_INS,VIAJE_ID,GDDIANOS.GET_IMPORTE_ITEM_FACTURA(MONTH(@FACT_FECHA_INICIO) ,YEAR(@FACT_FECHA_INICIO),@FACT_PERS_ID,VIAJE_ID)
				FROM GDDIANOS.VIAJE 
				WHERE VIAJE_CLIENTE_PERS_ID=@FACT_PERS_ID 
				AND MONTH(VIAJE_FECHA) = MONTH(@FACT_FECHA_INICIO) 
				AND YEAR(VIAJE_FECHA) = YEAR(@FACT_FECHA_INICIO)
			
				--Update del importe que al generar la cabecera esta en cero 

				UPDATE GDDIANOS.FACTURA SET FACT_IMPORTE_TOTAL=(SELECT SUM(ITEM_PRECIO_UNIT) FROM GDDIANOS.ITEM_FACTURA WHERE ITEM_FACT_ID=@FACT_ID_INS) WHERE FACT_ID=@FACT_ID_INS  

		END;
		ELSE
		BEGIN 
			RAISERROR('ERROR. NO SE PUEDE INSERTAR UNA FACTURA A UN CHOFER', 16, 1)
		END;
		
		FETCH NEXT FROM FACTURAS_A_INSERTAR INTO @FACT_ID_INS,@FACT_FECHA_GENERADA,@FACT_FECHA_INICIO,@FACT_FECHA_FIN,@FACT_IMPORTE_TOTAL,@FACT_PERS_ID,@PERS_TIPO
	END
	CLOSE FACTURAS_A_INSERTAR;
	DEALLOCATE FACTURAS_A_INSERTAR;
END;

